cuenta = float(input("Ingrese monto total de la cuenta: "))
prop10 = cuenta * 0.1
prop15 = cuenta * 0.15
print(f"Monto total: {cuenta}")
print(f"Propina al 10% = {prop10}")
print(f"Propina al 15% = {prop15}")
print(f"Total a pagar con 10% = {cuenta+prop10}")
print(f"Total a pagar con 15% = {cuenta+prop15}")